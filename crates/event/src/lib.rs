pub enum MouseButton {
    Primary = 0,
    Secondary = 1,
    Middle = 2,
    Extra1 = 3,
    Extra2 = 4,
}

pub enum Event {
    Drag {
        button: MouseButton,
        drag_begin: glam::Vec2,
        drag_end: glam::Vec2,
        modifiers: Modifiers,
    },
    Key {
        key: Key,
        pressed: bool,
        repeat: bool,
        modifiers: Modifiers,
    },
    PointerMoved(glam::Vec2),
    MouseMoved(glam::Vec2),
    MouseButton {
        pos: glam::Vec2,
        button: MouseButton,
        pressed: bool,
        modifiers: Modifiers,
    },
    MouseWheel {
        delta: glam::Vec2,
        modifiers: Modifiers,
    },
}
